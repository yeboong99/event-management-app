-- =============================================================
-- Task 43: approve_participation 동시성 테스트 - 데이터 정리
-- =============================================================
-- 실행 방법: psql "$DB_URL" -f supabase/tests/concurrent/05_cleanup.sql
-- 목적: 동시성 테스트에서 생성한 모든 데이터를 FK 역순으로 삭제
-- =============================================================

-- -------------------------------------------------------------
-- STEP 1: participations 삭제 (FK 의존성 가장 하위)
-- 테스트 이벤트에 연결된 모든 참여 신청 삭제
-- -------------------------------------------------------------
DELETE FROM participations
WHERE event_id IN (
  SELECT id FROM events
  WHERE title = '[TEST] Concurrent Approval Test'
);

-- -------------------------------------------------------------
-- STEP 2: events 삭제
-- 테스트 이벤트 삭제
-- -------------------------------------------------------------
DELETE FROM events
WHERE title = '[TEST] Concurrent Approval Test';

-- -------------------------------------------------------------
-- STEP 3: profiles 삭제
-- 고정 UUID 패턴 '00000000-0000-0000-%' 으로 식별하여 삭제
-- -------------------------------------------------------------
DELETE FROM profiles
WHERE id::text LIKE '00000000-0000-0000-%';

-- -------------------------------------------------------------
-- STEP 4: auth.users 삭제 (FK 의존성 최상위 → 마지막 삭제)
-- 고정 UUID 패턴 '00000000-0000-0000-%' 으로 식별하여 삭제
-- -------------------------------------------------------------
DELETE FROM auth.users
WHERE id::text LIKE '00000000-0000-0000-%';

-- -------------------------------------------------------------
-- 정리 완료 확인
-- -------------------------------------------------------------
SELECT 'Cleanup completed' AS status;
